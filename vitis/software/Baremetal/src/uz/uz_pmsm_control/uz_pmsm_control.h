#ifndef UZ_PMSM_CONTROL_H
#define UZ_PMSM_CONTROL_H

#include "../uz_Transformation/uz_Transformation.h"
#include "../uz_Space_Vector_Modulation/uz_space_vector_modulation.h"
#include "../uz_SpeedControl/uz_speedcontrol.h"
#include "../uz_CurrentControl/uz_CurrentControl.h"
#include "../uz_setpoint/uz_setpoint.h"
#include "../uz_controller_setpoint_filter/uz_controller_setpoint_filter.h"
#include "../uz_signals/uz_signals.h"

/**
 * @brief Object definition for PMSM control.
 */
typedef struct uz_pmsm_control_t uz_pmsm_control_t;

/**
 * @brief Safe operating region violation flags.
 */
enum uz_pmsm_control_safe_operating_region_violation
{
    uz_pmsm_control_no_violation = 0,      /**< No violation detected. */
    uz_pmsm_control_speed_violation_upper, /**< Mechanical speed exceeds upper limit. */
    uz_pmsm_control_speed_violation_lower, /**< Mechanical speed falls below lower limit. */
    uz_pmsm_control_i_d_violation_upper,   /**< d-current exceeds upper limit. */
    uz_pmsm_control_i_d_violation_lower,   /**< d-current falls below lower limit. */
    uz_pmsm_control_i_q_violation_upper,   /**< q-current exceeds upper limit. */
    uz_pmsm_control_i_q_violation_lower,   /**< q-current falls below lower limit. */
    uz_pmsm_control_i_abc_violation_upper, /**< Phase current magnitude exceeds upper limit. */
    uz_pmsm_control_v_dc_violation_upper,  /**< DC-link voltage exceeds upper limit. */
    uz_pmsm_control_v_dc_violation_lower,  /**< DC-link voltage falls below lower limit. */
    uz_pmsm_control_i_dc_violation_upper,  /**< DC-link current exceeds upper limit. */
    uz_pmsm_control_i_dc_violation_lower   /**< DC-link current falls below lower limit. */
};

/**
 * @brief Actual control state values generated by the PMSM control algorithm.
 */
struct uz_pmsm_actual_data
{
    uz_3ph_dq_t i_dq_in_A;          /**< Measured currents in dq-frame in A. */
    uz_3ph_dq_t v_dq_in_V;          /**< Control voltages in dq-frame in V. */
    float omega_el_rad_per_sec;     /**< Electrical angular speed in rad/s. */
    float speed_in_rpm;             /**< Mechanical speed in rpm. */
    float theta_el;                 /**< Electrical rotor angle in rad. */
    float theta_el_advanced;        /**< Delay-compensated electrical rotor angle in rad. */
};

/**
 * @brief Input measurement values for one PMSM control step.
 */
struct uz_pmsm_measurement_values
{
    uz_3ph_abc_t i_abc_in_A;        /**< Phase currents in abc-frame in A. */
    uz_3ph_abc_t v_abc_in_V;        /**< Phase voltages in abc-frame in V. */
    float v_dc_in_V;                /**< DC-link voltage in V. */
    float i_dc_in_A;                /**< DC-link current in A. */
    float omega_mech_rad_per_sec;   /**< Mechanical angular speed in rad/s. */
    float theta_mech;               /**< Mechanical rotor angle in rad. */
};

/**
 * @brief Internal reference values computed by PMSM control.
 */
struct uz_pmsm_reference_values
{
    float speed_in_rpm;                 /**< Speed reference in rpm. */
    float M_in_Nm;                      /**< Torque reference in Nm. */
    uz_3ph_dq_t i_dq_in_A;              /**< Current reference in dq-frame in A. */
    uz_3ph_dq_t v_dq_in_V;              /**< Voltage reference in dq-frame in V. */
    uz_3ph_abc_t v_abc_in_V;            /**< Voltage reference in abc-frame in V. */
    struct uz_DutyCycle_t duty_cycle;   /**< Generated duty cycle output. */
};

/**
 * @brief Generic scalar upper and lower bounds.
 */
struct uz_limits_t
{
    float upper_bound;  /**< Upper bound. */
    float lower_bound;  /**< Lower bound. */
};

/**
 * @brief Limits for reference inputs and generated setpoints.
 */
struct uz_pmsm_setpoint_limits_t
{
    struct uz_limits_t speed_controller_torque_in_Nm;   /**< Limits for speed-controller torque output in Nm. */
    struct uz_limits_t i_d_in_A;                        /**< Limits for d-current reference in A. */
    struct uz_limits_t i_q_in_A;                        /**< Limits for q-current reference in A. */
    struct uz_limits_t speed_in_rpm;                    /**< Limits for speed reference in rpm. */
    struct uz_limits_t disturbance_input_in_Nm;         /**< Limits for disturbance torque input in Nm. */
};

/**
 * @brief Safe operating region limits checked during runtime.
 */
struct uz_pmsm_safe_operating_region_t
{
    struct uz_limits_t speed_in_rpm;    /**< Allowed mechanical speed range in rpm. */
    struct uz_limits_t i_d_in_A;        /**< Allowed d-current range in A. */
    struct uz_limits_t i_q_in_A;        /**< Allowed q-current range in A. */
    struct uz_limits_t i_abc_in_A;      /**< Allowed phase current magnitude in A. */
    struct uz_limits_t v_dc_in_V;       /**< Allowed DC-link voltage range in V. */
    struct uz_limits_t i_dc_in_A;       /**< Allowed DC-link current range in A. */
};

/**
 * @brief Configuration for PMSM control module.
 */
struct uz_pmsm_control_configuration_t
{
    struct uz_pmsm_setpoint_limits_t setpoint_limits;                 /**< Limits applied to references and controller outputs. */
    struct uz_pmsm_safe_operating_region_t safe_operating_region;     /**< Safe operating region limits for fault detection. */
    float theta_el_offset;                                            /**< Electrical rotor angle offset in rad. */
    float sample_time;                                                /**< Control sample time in s, typically 1/PWM frequency. */
    bool enable_speed_control;                                        /**< Enable speed controller on initialization. */
    float speed_controller_kp;                                        /**< Proportional gain of speed controller. */
    float speed_controller_ki;                                        /**< Integral gain of speed controller. */
    float current_controller_d_kp;                                    /**< Proportional gain of d-current controller. */
    float current_controller_d_ki;                                    /**< Integral gain of d-current controller. */
    float current_controller_q_kp;                                    /**< Proportional gain of q-current controller. */
    float current_controller_q_ki;                                    /**< Integral gain of q-current controller. */
    float setpoint_filter_i_dq_cutoff_frequency;                      /**< Cutoff frequency for dq setpoint filter in Hz. Disable filter if 0. */
    float setpoint_filter_speed_cutoff_frequency;                     /**< Cutoff frequency for speed setpoint filter in Hz. Disable filter if 0. */
    float speed_actual_value_filter_cutoff_frequency;                 /**< Cutoff frequency for measured speed filter in Hz. Disable filter if 0. */
    enum uz_CurrentControl_decoupling_select decoupling_method;       /**< Decoupling method for current control. */
    enum uz_Setpoint_motor_type motor_type;                           /**< Motor type selection for setpoint generation. */
    bool enable_field_weakening;                                      /**< Enable field weakening in setpoint generation. */
    float relative_torque_tolerance;                                  /**< Relative torque tolerance for numerical solver. */
    float theta_sampling_compensation;                                /**< Multiplier for angle compensation based on sampling delay. */
    float theta_svm_delay_compensation;                               /**< Multiplier for SVM delay compensation in angle advance. */
    float voltage_theta_shift;                                        /**< Multiplier for shifting measurement angle used for voltage transformation. */
    struct uz_DutyCycle_t default_duty_cycle;                         /**< Duty cycle returned while control is disabled. */
};

/**
 * @brief Initializes PMSM control module.
 *
 * @param config Configuration struct for PMSM control.
 * @param machine_data Machine data for PMSM model parameters.
 * @return Pointer to initialized PMSM control instance.
 */
uz_pmsm_control_t *uz_pmsm_control_init(struct uz_pmsm_control_configuration_t config, uz_PMSM_t machine_data);

/**
 * @brief Returns pointer to internal actual control data.
 *
 * @param self Pointer to PMSM control instance.
 * @return Pointer to struct containing actual values.
 */
struct uz_pmsm_actual_data *uz_pmsm_control_get_actual_data(uz_pmsm_control_t *self);

/**
 * @brief Returns pointer to internal reference values.
 *
 * @param self Pointer to PMSM control instance.
 * @return Pointer to struct containing reference values.
 */
struct uz_pmsm_reference_values *uz_pmsm_control_get_reference_values(uz_pmsm_control_t *self);

/**
 * @brief Returns pointer to stored measurement values.
 *
 * @param self Pointer to PMSM control instance.
 * @return Pointer to struct containing latest measurement values.
 */
struct uz_pmsm_measurement_values *uz_pmsm_control_get_pmsm_measurement_values(uz_pmsm_control_t *self);

/**
 * @brief Enables or disables the controller output stage.
 *
 * @param self Pointer to PMSM control instance.
 * @param enable Set true to enable control output, false to disable.
 */
void uz_pmsm_control_enable(uz_pmsm_control_t *self, bool enable);

/**
 * @brief Executes one control step and returns duty cycle output.
 *
 * @param self Pointer to PMSM control instance.
 * @param measurements Latest measured values.
 * @param reference_speed_in_rpm Speed reference in rpm.
 * @param reference_currents Current references in dq-frame in A.
 * @param disturbance_input_in_Nm Disturbance torque feedforward in Nm.
 * @return Duty cycle command for inverter.
 */
struct uz_DutyCycle_t uz_pmsm_control_sample_duty(uz_pmsm_control_t *self, struct uz_pmsm_measurement_values measurements, float reference_speed_in_rpm, uz_3ph_dq_t reference_currents, float disturbance_input_in_Nm);

/**
 * @brief Executes one control step and returns dq-voltage output.
 *
 * @param self Pointer to PMSM control instance.
 * @param measurements Latest measured values.
 * @param reference_speed_in_rpm Speed reference in rpm.
 * @param reference_currents Current references in dq-frame in A.
 * @param disturbance_input_in_Nm Disturbance torque feedforward in Nm.
 * @return dq-voltage command in V.
 */
struct uz_3ph_dq_t uz_pmsm_control_sample_dq(uz_pmsm_control_t *self, struct uz_pmsm_measurement_values measurements, float reference_speed_in_rpm, uz_3ph_dq_t reference_currents, float disturbance_input_in_Nm);

/**
 * @brief Resets internal controller states.
 *
 * @param self Pointer to PMSM control instance.
 */
void uz_pmsm_control_reset(uz_pmsm_control_t *self);

/**
 * @brief Enables or disables internal speed control loop.
 *
 * @param self Pointer to PMSM control instance.
 * @param enable_speed_control Set true to enable speed control.
 */
void uz_pmsm_control_enable_speed_control(uz_pmsm_control_t *self, bool enable_speed_control);

/**
 * @brief Acknowledges a safe operating region fault and reevaluates with current measurements.
 *
 * @param self Pointer to PMSM control instance.
 * @param measurements Latest measured values.
 */
void uz_pmsm_control_acknowledge_and_reset_error(uz_pmsm_control_t *self, struct uz_pmsm_measurement_values measurements);

/**
 * @brief Returns current safe operating region violation state.
 *
 * @param self Pointer to PMSM control instance.
 * @return Active safe operating region violation flag.
 */
enum uz_pmsm_control_safe_operating_region_violation uz_pmsm_control_get_safe_operating_area_violation(uz_pmsm_control_t *self);

/**
 * @brief Sets electrical angle offset.
 *
 * @param self Pointer to PMSM control instance.
 * @param theta_offset Electrical angle offset in rad.
 */
void uz_pmsm_control_set_theta_offset(uz_pmsm_control_t *self, float theta_offset);

/**
 * @brief Returns pointer to electrical angle offset variable.
 *
 * @param self Pointer to PMSM control instance.
 * @return Pointer to theta offset in rad.
 */
float *uz_pmsm_control_get_pointer_to_theta_offset(uz_pmsm_control_t *self);

/**
 * @brief Tunes current controller gains using magnitude optimum.
 *
 * @param self Pointer to PMSM control instance.
 * @param tau_sigma_sec Equivalent delay time constant in s.
 */
void uz_pmsm_control_current_control_tune_magnitude_optimum(uz_pmsm_control_t *self, float tau_sigma_sec);

/**
 * @brief Tunes current controller gains using symmetric optimum.
 *
 * @param self Pointer to PMSM control instance.
 * @param tau_sigma_sec Equivalent delay time constant in s.
 */
void uz_pmsm_control_current_control_tune_symmetric_optimum(uz_pmsm_control_t *self, float tau_sigma_sec);

/**
 * @brief Tunes current controller gains for a target bandwidth.
 *
 * @param self Pointer to PMSM control instance.
 * @param bandwidth_rad_per_sec Desired bandwidth in rad/s.
 */
void uz_pmsm_control_current_control_tune_bandwidth(uz_pmsm_control_t *self, float bandwidth_rad_per_sec);

/**
 * @brief Sets proportional gain of q-current controller.
 *
 * @param self Pointer to PMSM control instance.
 * @param Kp_iq Proportional gain for q-current loop.
 */
void uz_pmsm_control_current_control_set_Kp_iq(uz_pmsm_control_t *self, float Kp_iq);

/**
 * @brief Sets integral gain of q-current controller.
 *
 * @param self Pointer to PMSM control instance.
 * @param Ki_iq Integral gain for q-current loop.
 */
void uz_pmsm_control_current_control_set_Ki_iq(uz_pmsm_control_t *self, float Ki_iq);

/**
 * @brief Sets proportional gain of d-current controller.
 *
 * @param self Pointer to PMSM control instance.
 * @param Kp_id Proportional gain for d-current loop.
 */
void uz_pmsm_control_current_control_set_Kp_id(uz_pmsm_control_t *self, float Kp_id);

/**
 * @brief Sets integral gain of d-current controller.
 *
 * @param self Pointer to PMSM control instance.
 * @param Ki_id Integral gain for d-current loop.
 */
void uz_pmsm_control_current_control_set_Ki_id(uz_pmsm_control_t *self, float Ki_id);

/**
 * @brief Sets proportional gain of speed controller.
 *
 * @param self Pointer to PMSM control instance.
 * @param Kp_speed Proportional gain of speed loop.
 */
void uz_pmsm_control_speed_control_set_Kp_speed(uz_pmsm_control_t *self, float Kp_speed);

/**
 * @brief Sets integral gain of speed controller.
 *
 * @param self Pointer to PMSM control instance.
 * @param Ki_speed Integral gain of speed loop.
 */
void uz_pmsm_control_speed_control_set_Ki_speed(uz_pmsm_control_t *self, float Ki_speed);

#endif // UZ_PMSM_CONTROL_H
